// These values are used to generate the high score verification codes.
// Note that these values are not the same as the set of values used in the official
// binary as well as the web page.
// Therefore, any derivative or user-compiled binaries will not be able to generate
// valid high scores for submission to the Hall of Fame on the web.

#define MAGIC_NUMBER_A 0xE1
#define MAGIC_NUMBER_B 0xD5
#define MAGIC_NUMBER_C 0x8F
#define MAGIC_NUMBER_D 0x42
#define MAGIC_NUMBER_E 0x99
#define MAGIC_NUMBER_F 0xE3
#define MAGIC_NUMBER_G 0x10
#define MAGIC_NUMBER_H 0xC3
